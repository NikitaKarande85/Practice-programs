// 2.  Write a program which accept one number from user and check whether that 
// number is greater than 100 or not. 
// Input : 101  
// Output : Greater  
// Input : 39  
// Output : Smaller  

function print(no) {
    if(no>=100){
        console.log("Greater");
    }else{
        console.log("Smaller");
    }
}

print(10)
